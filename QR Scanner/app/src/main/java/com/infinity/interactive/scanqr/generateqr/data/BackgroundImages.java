package com.infinity.interactive.scanqr.generateqr.data;

public class BackgroundImages {

    int QRBackground;
    int QRImage;

    public BackgroundImages(int QRBackground, int QRImage) {
        this.QRBackground = QRBackground;
        this.QRImage = QRImage;
    }

    public int getQRBackground() {
        return QRBackground;
    }

    public void setQRBackground(int QRBackground) {
        this.QRBackground = QRBackground;
    }

    public int getQRImage() {
        return QRImage;
    }

    public void setQRImage(int QRImage) {
        this.QRImage = QRImage;
    }

}
